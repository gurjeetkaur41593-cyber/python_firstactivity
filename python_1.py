# ---------------------------------------------
# Demonstration of Python Data Types in a Table
# ---------------------------------------------

# String: Used for storing textual data such as names
name = "John Doe"

# Integer: Whole number type, useful for counts or ages
age = 28

# List: Ordered, changeable collection — ideal for storing multiple skills
skills = ["Python", "SQL", "Power BI"]

# Tuple: Ordered, unchangeable collection — good for fixed education details
education = ("BSc Computer Science", 2020)

# Dictionary: Key-value pairs
contact_details = {"email": "john.doe@email.com", "phone": "+123456789"}

# Set: Unordered collection of unique items — duplicates removed automatically
certifications = {"Azure", "AWS", "Azure"}  # duplicate "Azure" removed

# Create a table-like output
print(name)
print(age)
print(skills)
print(education)
print(contact_details)
print(certifications)